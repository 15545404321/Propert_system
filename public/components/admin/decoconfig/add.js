Vue.component('Add', {
	template: `
		<el-dialog title="添加" width="600px" class="icon-dialog" :visible.sync="show" @open="open" :before-close="closeForm" append-to-body>
			<el-form :size="size" ref="form" :model="form" :rules="rules" :label-width=" ismobile()?'90px':'16%'">
				<el-row >
					<el-col :span="24">
						<el-form-item label="页面名称" prop="decoconfig_name">
							<el-input  v-model="form.decoconfig_name" autoComplete="off" clearable  placeholder="请输入页面名称"></el-input>
						</el-form-item>
					</el-col>
				</el-row>
				<el-row >
					<el-col :span="24">
						<el-form-item label="页面路径" prop="decoconfig_url">
							<el-input  v-model="form.decoconfig_url" autoComplete="off" clearable  placeholder="请输入页面路径"></el-input>
						</el-form-item>
					</el-col>
				</el-row>
				<el-row >
					<el-col :span="24">
						<el-form-item label="页面属性" prop="decoconfig_type">
							<el-select style="width:100%" v-model="form.decoconfig_type" filterable clearable placeholder="请选择页面属性">
								<el-option key="0" label="tab页" :value="1"></el-option>
								<el-option key="1" label="普通页" :value="2"></el-option>
								<el-option key="2" label="弹窗" :value="3"></el-option>
							</el-select>
						</el-form-item>
					</el-col>
				</el-row>
				<el-row >
					<el-col :span="24">
						<el-form-item label="页面备注" prop="decoconfig_remark">
							<el-input  v-model="form.decoconfig_remark" autoComplete="off" clearable  placeholder="请输入页面备注"></el-input>
						</el-form-item>
					</el-col>
				</el-row>
			</el-form>
			<div slot="footer" class="dialog-footer">
				<el-button :size="size" :loading="loading" type="primary" @click="submit" >
					<span v-if="!loading">确 定</span>
					<span v-else>提 交 中...</span>
				</el-button>
				<el-button :size="size" @click="closeForm">取 消</el-button>
			</div>
		</el-dialog>
	`
	,
	components:{
	},
	props: {
		show: {
			type: Boolean,
			default: false
		},
		size: {
			type: String,
			default: 'small'
		},
	},
	data(){
		return {
			form: {
				decoconfig_name:'',
				decoconfig_url:'',
				decoconfig_remark:'',
			},
			loading:false,
			rules: {
				decoconfig_name:[
					{required: true, message: '页面名称不能为空', trigger: 'blur'},
				],
			}
		}
	},
	methods: {
		open(){
		},
		submit(){
			this.$refs['form'].validate(valid => {
				if(valid) {
					this.loading = true
					axios.post(base_url + '/DecoConfig/add',this.form).then(res => {
						if(res.data.status == 200){
							this.$message({message: res.data.msg, type: 'success'})
							this.$emit('refesh_list')
							this.closeForm()
						}else{
							this.loading = false
							this.$message.error(res.data.msg)
						}
					}).catch(()=>{
						this.loading = false
					})
				}
			})
		},
		closeForm(){
			this.$emit('update:show', false)
			this.loading = false
			if (this.$refs['form']!==undefined) {
				this.$refs['form'].resetFields()
			}
		},
	}
})
